package af.swing.layout;

public class AfPadding
{
	// 按照 Swing 的排列顺序，应该是 上 左 下 右
	public int top, left, bottom, right;
	
	public AfPadding()
	{		
	}
	
	public AfPadding(int value)
	{
		top = left = bottom = right;
	}
	
	public AfPadding(int hValue, int vValue)
	{
		left = right = hValue;
		top = bottom = vValue;
	}
	
	public AfPadding(int top, int left, int bottom, int right)
	{
		this.top = top;
		this.left = left;
		this.bottom = bottom;
		this.right = right;
	}
}
